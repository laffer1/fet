// Description  : Determine if Item is reachable
qboolean
AIMOD_ITEMS_IsReachable ( gentity_t *uniquebot, vec3_t goal )
{
	trace_t trace;
	vec3_t	v;
	VectorCopy( uniquebot->r.mins, v );
	v[2] += 18; // Stepsize
	trap_Trace( &trace, uniquebot->s.origin, v, uniquebot->r.maxs, goal, uniquebot->s.number, MASK_SOLID | MASK_OPAQUE );

	//trace = gi.trace (uniquebot->s.origin, v, uniquebot->maxs, goal, uniquebot, MASK_SOLID|MASK_OPAQUE);
	// Yes we can see it
	//-------------------------------------------------------
	if ( trace.fraction == 1.0 )
	{
		return ( qtrue );
	}
	else
	{
		return ( qfalse );
	}

	//-------------------------------------------------------
}


