int
fontBitmapString ( char *str )
{
	int		len, i;
	GLint	swapbytes, lsbfirst, rowlength;
	GLint	skiprows, skippixels, alignment;
	len = (int) strlen( str );
	GLV.glGetIntegerv( GL_UNPACK_SWAP_BYTES, &swapbytes );
	GLV.glGetIntegerv( GL_UNPACK_LSB_FIRST, &lsbfirst );
	GLV.glGetIntegerv( GL_UNPACK_ROW_LENGTH, &rowlength );
	GLV.glGetIntegerv( GL_UNPACK_SKIP_ROWS, &skiprows );
	GLV.glGetIntegerv( GL_UNPACK_SKIP_PIXELS, &skippixels );
	GLV.glGetIntegerv( GL_UNPACK_ALIGNMENT, &alignment );
	GLV.glPixelStorei( GL_UNPACK_SWAP_BYTES, GL_FALSE );
	GLV.glPixelStorei( GL_UNPACK_LSB_FIRST, GL_FALSE );
	GLV.glPixelStorei( GL_UNPACK_ROW_LENGTH, 0 );
	GLV.glPixelStorei( GL_UNPACK_SKIP_ROWS, 0 );
	GLV.glPixelStorei( GL_UNPACK_SKIP_PIXELS, 0 );
	GLV.glPixelStorei( GL_UNPACK_ALIGNMENT, 1 );
	for ( i = 0; i < len; i++ )
	{
		char	ch = str[i];
		if ( ch == 0 )
		{	/* end of string */
			break;
		}

		if ( ch < FIRST )
		{
			continue;
		}

		if ( ch > LAST )
		{
			continue;
		}

		ch -= FIRST;
		glBitmap( 8, 13, 0.0, 2.0, 10.0, 0.0, &bitmapFont[ch][1] );
	}

	GLV.glPixelStorei( GL_UNPACK_SWAP_BYTES, swapbytes );
	GLV.glPixelStorei( GL_UNPACK_LSB_FIRST, lsbfirst );
	GLV.glPixelStorei( GL_UNPACK_ROW_LENGTH, rowlength );
	GLV.glPixelStorei( GL_UNPACK_SKIP_ROWS, skiprows );
	GLV.glPixelStorei( GL_UNPACK_SKIP_PIXELS, skippixels );
	GLV.glPixelStorei( GL_UNPACK_ALIGNMENT, alignment );
	return ( 1 );
}
